u16 ccitt16(u8* data, u32 len) {
	u16 crc = 0xFFFF;

	for (u32 i = 0; i < len; i++) {
		crc ^= (u16) (data[i] << 8);

		for (u32 j = 0; j < 0x8; j++) {
			if ((crc & 0x8000) > 0)
				crc = (u16) ((crc << 1) ^ 0x1021);
			else
				crc <<= 1;
		}
	}

	return crc;
}

u16 check16(u8 data[], u32 blockID, u32 len) {
	u16 initial = 0;
	
	if (blockID == 36) {
		u8 tmp[0x80];
		memset(tmp, 0, 0x80);
		memcpy(&data[0x100], tmp, 0x80);
	}

	u16 chk = ~initial;
	for (u32 i = 0; i < len; i++)
		chk = (crc16[(data[i] ^ chk) & 0xFF] ^ chk >> 8);

	return ~chk;
}

void rewriteCHK(u8 *mainbuf) {
	u8 blockCount = 0;
	u8* tmp = (u8*)malloc(0x36600 * sizeof(u8));
	u16 cs;
	u32 csoff = 0;
	
	if (game_getisXY()) {
		blockCount = 55;
		csoff = 0x6A81A - 0x5400;
	}

	else if (game_getisORAS()) {
		blockCount = 58;
		csoff = 0x7B21A - 0x5400;
	}
	
	else if (game_getisSUMO()) {
		blockCount = 37;
		csoff = 0x6BE00 - 0x200 + 0x10 + 0x0A;
	}
	
	else if (game_getisUSUM()) {
		blockCount = 39;
		csoff = 0x6CA1A;		
	}

	if (game_isgen6())
		for (u32 i = 0; i < blockCount; i++) {
			memcpy(tmp, mainbuf + CHKOffset(i), CHKLength(i));
			cs = ccitt16(tmp, CHKLength(i));
			memcpy(mainbuf + csoff + i * 8, &cs, 2);
		}
		
	else if (game_isgen7()) {
		for (u32 i = 0; i < blockCount; i++) {
			memcpy(tmp, mainbuf + CHKOffset(i), CHKLength(i));
			cs = check16(tmp, getBlockID(mainbuf, csoff, i), CHKLength(i));
			memcpy(mainbuf + csoff + i * 8, &cs, 2);
		}
		
		resign(mainbuf);
	}

	else if (game_isgen5()) {
		int game = game_get();
		if (game == GAME_B1 || game == GAME_W1)
			blockCount = 70;
		else
			blockCount = 74;
		for (u32 i = 0; i < blockCount; i++) {
			memcpy(tmp, mainbuf + CHKOffset(i), CHKLength(i));
			cs = ccitt16(tmp, CHKLength(i));
			memcpy(mainbuf + BWCHKOff(i), &cs, 2);
			memcpy(mainbuf + BWCHKMirr(i), &cs, 2);
		}
	}
	
	else if (game_isgen4()) {
		int game = game_get();
		// start, end, chkoffset
		int general[3] = {0, 0, 0};
		int storage[3] = {0, 0, 0};

		if (game == GAME_DIAMOND || game == GAME_PEARL) {
			general[0] = 0x0000; general[1] = 0xC0EC; general[2] = 0xC0FE;
			storage[0] = 0xC100; storage[1] = 0x1E2CC; storage[2] = 0x1E2DE;
		} 
		else if (game == GAME_PLATINUM) {
			general[0] = 0x0000; general[1] = 0xCF18; general[2] = 0xCF2A;
			storage[0] = 0xCF2C; storage[1] = 0x1F0FC; storage[2] = 0x1F10E;
		}
		else if (game == GAME_HG || game == GAME_SS) {
			general[0] = 0x0000; general[1] = 0xF618; general[2] = 0xF626;
			storage[0] = 0xF700; storage[1] = 0x21A00; storage[2] = 0x21A0E;		
		}
		
		memcpy(tmp, mainbuf + gbo + general[0], general[1] - general[0]);
		cs = ccitt16(tmp, general[1] - general[0]);
		memcpy(mainbuf + gbo + general[2], &cs, 2);

		memcpy(tmp, mainbuf + sbo + storage[0], storage[1] - storage[0]);
		cs = ccitt16(tmp, storage[1] - storage[0]);
		memcpy(mainbuf + sbo + storage[2], &cs, 2);		
	}

	free(tmp);
}

void resign(u8 *mainbuf) {
	if (!game_isgen7())
	{
		return;
	}
	
	const bool isUSUM = game_getisUSUM();
	const int chkTblOfs = isUSUM ? 0x6CA00 : 0x6BC00;
	const int memeOfs = isUSUM ? 0x6C100 : 0x6BB00;
	const int chkTblLen = isUSUM ? 0x150 : 0x140;
	
	u8 CurSig[0x80];
	memcpy(CurSig, &mainbuf[memeOfs], 0x80);

	u8 ChecksumTable[chkTblLen];
	memcpy(ChecksumTable, &mainbuf[chkTblOfs], chkTblLen);
	
	u8 Hash[SHA256_BLOCK_SIZE];
	sha256(Hash, ChecksumTable, chkTblLen);
	
	u8 DecSig[0x80];
	reverseCrypt(CurSig, DecSig);
	memcpy(DecSig, Hash, SHA256_BLOCK_SIZE);
	
	memecrypto_sign(DecSig, CurSig, 0x80);
	memcpy(&mainbuf[memeOfs], CurSig, 0x80);
}